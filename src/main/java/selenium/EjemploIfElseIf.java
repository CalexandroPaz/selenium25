package selenium;

public class EjemploIfElseIf {
	
	public static void main(String[] args) {
		
		int i = 17;
		if(i == 10) {
			System.out.println("i igual a 10");
		}else if (i == 15) {
			System.out.println("i igual a 15");
		}else if (i == 20) {
			System.out.println("i igual a 20");
		}else {
			System.out.println("i no equivale a ninguna opccion");
		}
	}

}
